class _MuscleExercisePageState extends State<MuscleExercisePage> {
  late ScrollController _scrollController; // Контроллер для управления прокруткой

  @override
  void initState() {
    super.initState();
    final muscleSelectionModel = Provider.of<MuscleSelectionModel>(context, listen: false);

    // Инициализируем контроллер и восстанавливаем позицию прокрутки
    _scrollController = ScrollController(
      initialScrollOffset: muscleSelectionModel.scrollPosition,
    );

    // Слушаем изменения в прокрутке и сохраняем позицию
    _scrollController.addListener(() {
      muscleSelectionModel.setScrollPosition(_scrollController.offset);
    });
  }

  @override
  void dispose() {
    _scrollController.dispose(); // Освобождаем контроллер при завершении
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    final muscleSelectionModel = Provider.of<MuscleSelectionModel>(context);
    final favoriteModel = Provider.of<FavoriteModel>(context); // Доступ к избранному

    final selectedMuscleGroup = muscleSelectionModel.selectedMuscleGroup; // Получаем текущую выбранную группу

    return Scaffold(
      appBar: AppBar(
        centerTitle: true,
        title: Text(selectedMuscleGroup ?? 'Группы мышц'),  // Заголовок: либо группа мышц, либо "Группы мышц"
