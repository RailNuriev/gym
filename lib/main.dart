        )
            : null,  // Если нет выбранной группы мышц, стрелки "назад" нет
      ),
      body: selectedMuscleGroup == null
          ? GridView.count(
        controller: _scrollController, // Используем контроллер прокрутки
        crossAxisCount: 2, // Количество столбцов
        crossAxisSpacing: 8,
        mainAxisSpacing: 8,
        padding: EdgeInsets.all(10),
        children: List.generate(muscleGroups.length, (index) {
