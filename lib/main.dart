// Модель для избранного с добавлением SharedPreferences
class FavoriteModel extends ChangeNotifier {
  final Set<String> _favorites = {}; // Хранит названия избранных упражнений
  static const String _favoritesKey = 'favorite_exercises';

  FavoriteModel() {
    _loadFavorites(); // Загружаем данные при создании модели
  }

  Set<String> get favorites => _favorites;

  // Загрузка списка избранного из локального хранилища
  Future<void> _loadFavorites() async {
    final prefs = await SharedPreferences.getInstance();
    final favoritesString = prefs.getString(_favoritesKey);
    if (favoritesString != null) {
      _favorites.addAll(List<String>.from(jsonDecode(favoritesString)));
      notifyListeners(); // Уведомляем интерфейс о необходимости обновления
    }
  }

  // Сохранение списка избранного в локальное хранилище
  Future<void> _saveFavorites() async {
    final prefs = await SharedPreferences.getInstance();
    prefs.setString(_favoritesKey, jsonEncode(_favorites.toList()));
  }

  // Добавление или удаление упражнения из избранного
  void toggleFavorite(String exerciseName) {
    if (_favorites.contains(exerciseName)) {
      _favorites.remove(exerciseName);
    } else {
      _favorites.add(exerciseName);
    }
    _saveFavorites(); // Сохраняем изменения в избранном
    notifyListeners();
  }

  bool isFavorite(String exerciseName) {
    return _favorites.contains(exerciseName);
  }
}


