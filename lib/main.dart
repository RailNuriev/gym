        // Находим упражнение в общем списке всех упражнений
        final exercise = allExercises.firstWhere(
              (e) => e['name'] == exerciseName,
          orElse: () => {'name': 'Упражнение не найдено', 'imageUrl': '', 'description': ''}, // Пустая карта вместо null
        );

        return Card(
          child: ListTile(
            leading: exercise['imageUrl']!.isNotEmpty
                ? Image.network(exercise['imageUrl']!) // Картинка упражнения
                : Icon(Icons.image_not_supported), // Пустая иконка, если изображения нет
            title: Text(exercise['name']!), // Название упражнения
            trailing: IconButton(
              icon: Icon(
                Icons.favorite,
                color: Colors.red,
              ),
              onPressed: () {
                favoriteModel.toggleFavorite(exercise['name']!); // Убираем из избранного
              },
            ),
            onTap: () {
              if (exercise['description']!.isNotEmpty) {
                // Переход к подробной странице упражнения, если есть описание
                Navigator.push(
                  context,
                  MaterialPageRoute(
                    builder: (context) => ExerciseDetailPage(
                      exerciseName: exercise['name']!,
                      imageUrl: exercise['imageUrl']!,
                      description: exercise['description']!,
                    ),
                  ),
                );
              }
            },
          ),
        );
      },
    );
  }
}

